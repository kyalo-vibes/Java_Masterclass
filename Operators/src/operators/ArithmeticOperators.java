package operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        
        // Assignment Operator
        int i = 20;
        double d = 10.1;
        
        // Additive operator
        System.out.println("i + d = " + (i + d));
        
        // Subtraction operator
        System.out.println("i - d = " + (i - d));
        
        // Multiplication operator
        System.out.println("i * d = " + (i * d));
        
        // Division operator
        System.out.println("i / d = " + (i / d));
        
        // Remainder or Modulus operator
        System.out.println("i % d = " + (i % d));
    }
}