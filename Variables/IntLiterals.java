public class IntLiterals{
    public static void main(String[] args) {

	// Integer Literals

	byte b = 120;

	short s = 1000;

	int intVal = 2000;
	
	int hexVal = 0x7D0; //Hexadecimal representation of 2000

	int binVal = 0b11111010000; //Binary representation of 2000

	long l = 922337;

	System.out.println("byte : "+ b);

	System.out.println("Short : "+ s);

	System.out.println("Integer value : "+ intVal);

	System.out.println("Hex Val : "+ hexVal);

	System.out.println("Bin Val : "+ binVal);

	System.out.println("Long : "+ l);
    }
}