public class Char{
    public static void main(String[] args) {

	char c1 = 76; // Unicode for L
	char c2 = 40; // Unicode for '('
	char c3 = 'A'; // unicode 65

	System.out.println("Char for unicode 76 is : "+ c1);

	System.out.println("Char for unicode 40 is : "+ c2);

	System.out.println("Display char c3 : "+ c3);

	c3++;

	System.out.println("Display char c3++ : "+ c3);
    }
}
