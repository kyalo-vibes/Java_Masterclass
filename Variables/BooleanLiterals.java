public class BooleanLiterals{
    public static void main(String[] args) {

	// Boolean Literals
	
	boolean trueVal = true;

	boolean falseVal = false;

	System.out.println("True : "+ trueVal);

	System.out.println("False : "+ falseVal);

	System.out.println("Is 10 greater than 100 ? : "+ (10 > 100));

	System.out.println("Is 100 greater than 10 ? : "+ (100 > 10));
    }
}