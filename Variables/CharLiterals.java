public class CharLiterals{
    public static void main(String[] args) {

	// Character Literals

	char uniVal = 65;

	char c = 'A';

	char escSeq = '\t';

	System.out.println("uniVal : "+ uniVal);

	System.out.println("char c : "+ c);

	System.out.println("escSeq : "+ escSeq +"Text after escape sequence");
    }
}